#include <iostream>
#include <cmath>

using namespace std;

int main()
{
    cout <<ceil(2.3)<<"\n"<<cos(180)<<"\n"<<exp(1.0)<<"\n"<<fabs(-8.8)<<endl;
    cout<<floor(9.2)<<"\n"<<fmod(1.8,0.9)<<"\n"<<log(2.71828)<<"\n"<<log10(10.0)<<endl;
    cout<<pow(2,7)<<"\n"<<sin(0.0)<<"\n"<<sqrt(9.0)<<"\n"<<tan(0.0)<<endl;
    return 0;
}
